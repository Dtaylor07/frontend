import React, { useState, useEffect } from 'react';
import api from './api';

function App() {
  const [data, setData] = useState('');

  useEffect(() => {
    api.get('/')
      .then(response => {
        setData(response.data);
      })
      .catch(error => {
        console.error(error);
      });
  }, []);

  return (
    <div>
      <h1>{data}</h1>
    </div>
  );
}